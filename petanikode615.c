#include <stdio.h>

void main(){
    int a = 4;

    // increment a
    a++;

    printf("a++ = %d\n", a);

    // increment a
    ++a;

    printf("++a = %d\n", a);

    // decrement a
    a--;

    printf("a-- = %d\n", a);

    //increment dan decrment untuk tambah atau kurang 1

    // decrement a
    --a;

    printf("--a = %d\n", a);
}
